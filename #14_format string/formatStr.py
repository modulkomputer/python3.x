# contoh generic
nama = "muhaemin"
format_str = f"hello: {nama}"
print(format_str)


# menggunakan boolean
bool = True
format_str = f"hello: {bool}"
print(format_str)

# angka
angka = 2005.5
format_str = f"angka: {angka}"
print(format_str)

# bilangan bulat
angka = 12
format_str = f"angka: {angka: d}"
print(format_str)

# angka ribuan / jutaan
angka = 2000000
format_str = f"angka: {angka}"
print(format_str)

# bilangan decimal
angka = 20054.32445
format_str = f"angka: {angka: 010.37}"
print(format_str)

persentase = 0.034
format_persen = f"persen: {persentase: %}"
print(format_persen)
